     */
    public function create($id)
    {
        $Product = Product::find($id);
        Cart::add(['id' => $Product->id, 'name' => $Product->pro_name, 'qty' => 1, 'price' => $Product->pro_price,'options' => ['image' => $Product->pro_img]]);
        return redirect('/cart');

    }

